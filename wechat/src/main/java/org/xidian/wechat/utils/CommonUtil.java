	/**
	 * 
	 * @param appid
	 * @param appsecret
	 * @return
	 */
	public static Token getToken(String appid, String appsecret) {
		Token token = null;
		String requestUrl = WXConfig.TOKEN_URL.replace("APPID", appid).replace("APPSECRET", appsecret);
		//统一为fastjson写法
		//JSONObject jsonObject = JSONObject.fromObject(httpsRequest(requestUrl, "GET", null));
		JSONObject jsonObject = (JSONObject) JSONObject.parse(httpsRequest(requestUrl, "GET", null));
		if (null != jsonObject) {
			try {
				token = new Token();
				token.setAccessToken(jsonObject.getString("access_token"));
				//token.setExpiresIn(jsonObject.getInt("expires_in"));
				token.setExpiresIn(jsonObject.getIntValue("expires_in"));
			} catch (Exception e) {
				token = null;
			}
		}
		return token;
	}
	
	public static String urlEncodeUTF8(String source){
		String result = source;
		try {
			result = java.net.URLEncoder.encode(source,"utf-8");
		} catch (UnsupportedEncodingException e) {
			e.printStackTrace();
		}
		return result;
	}
	
	
	
	
